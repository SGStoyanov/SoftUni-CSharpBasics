using System;
class DeclareVariables
{
    static void Main()
    {
        sbyte mySbyte = -115;
        byte myByte = 97;
        short myShort2 = -10000;
        int myInt = 52130;
        int myInt2 = 4825932;

        Console.WriteLine("My first sbyte: " + mySbyte);
        Console.WriteLine("My first byte: " + myByte);
        Console.WriteLine("My first short: " + myShort2);
        Console.WriteLine("My first int: " + myInt);
        Console.WriteLine("My second int: " + myInt2);
    }
}